#include <iostream>
using namespace std;

int main(){
    int a=5,b=6;
    //These are some arithamatics operators
    cout<<"The value of a+b="<<a+b<<endl;
    cout<<"The value of a-b="<<a-b<<endl;
    cout<<"The value of a*b="<<a*b<<endl;
    cout<<"The value of a/b="<<a/b<<endl;
    cout<<"The value of a%b="<<a%b<<endl;

    //The value of ++a add 1 in a and then print,e.g.
     cout<<"The value of a++="<<++a<<endl;
    //The value of a++ add 1 in the next value and then print in next,e.g.
    cout<<"The value of a++="<<a++<<endl;
    cout<<"The value of a++="<<a<<endl;
    //Here you can see that it take the value and then add one and print it
    return 0;
}