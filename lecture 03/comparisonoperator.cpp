#include <iostream>
using namespace std;

int main()
{
    //these are some examples of comparison opperator
    int a=5,b=4;
    cout <<"The value of a==b "<<(a==b)<<endl;
    cout <<"The value of a<=b "<<(a<=b)<<endl;
    cout <<"The value of a>=b "<<(a>=b)<<endl;
    cout <<"The value of a<b "<<(a<b)<<endl;
    cout <<"The value of a>b "<<(a>b)<<endl;
    return 0;
}
