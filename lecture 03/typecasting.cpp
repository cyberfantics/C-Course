#include <iostream>
using namespace std;
int main(){
  
  //type casting is used to change the type of variable
    float x = 56.7f;
    int y = 546;
    //first method for changing the type of variable
    cout<<(int)x<<endl;
     cout<<(float)y<<endl;
    //second method for changing the type of variable
    cout<<int(x)<<endl;
    cout<<float(y)<<endl;
    return 0;

}